export async function generateContentForOption(option) {
  if (!option || !option.title) {
    console.error("generateContentForOption: Invalid option provided.");
    return "Error: Could not generate details because the selected option is invalid.";
  }

  try {
    const response = await fetch('/api/generate-content', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        title: option.title,
        description: option.description
      }),
    });

    if (!response.ok) {
      throw new Error(`Server returned ${response.status}`);
    }

    // Try to parse as JSON, but handle HTML responses properly
    const contentType = response.headers.get('content-type');
    if (contentType && contentType.includes('application/json')) {
      const data = await response.json();
      return data.content;
    } else {
      // If we received HTML or other non-JSON content, use fallback
      console.warn(`Received non-JSON response for ${option.title}. Using fallback.`);
      return getFallbackContent(option);
    }
  } catch (error) {
    console.error(`Content generation error for ${option.title}:`, error);
    return getFallbackContent(option);
  }
}

export function getFallbackContent(option) {
  return `${option.title} is a popular option for Arts students.\n\n${option.description}\n\nStudents who pursue this path can expect to gain valuable skills and knowledge in their chosen field. The curriculum typically includes specialized courses that build upon the foundation established through Arts studies.\n\nKey benefits include industry recognition, strong career prospects, and the opportunity to make significant contributions to the field.\n\nFor more detailed information, including admission requirements, curriculum details, and career outcomes, visit the official website.`;
}
